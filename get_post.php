<?php
//  GET
// 1. **طريقة الإرسال**: يتم إرسال البيانات كجزء من عنوان URL.
// 2. **عرض البيانات**: البيانات المُرسلة تُعرض في شريط عنوان المتصفح، مما يجعلها مرئية وغير آمنة لإرسال معلومات حساسة.
// 3. **الطول المحدد**: هناك قيود على طول عنوان URL، لذلك GET غير مناسب لإرسال كميات كبيرة من البيانات.
// 4. **التخزين المؤقت**: طلبات GET يمكن تخزينها في ذاكرة التخزين المؤقت للمتصفح ويمكن أيضًا أن تُضاف إلى سجل المتصفح.
// 5. **استخدامات شائعة**: تُستخدم عادةً للحصول على البيانات من الخادم، مثل جلب صفحة أو استعلام في محرك بحث.
if (isset($_GET['name'])) {
    echo 'Hello, ' . htmlspecialchars($_GET['name']) . '!';
}

//POST1.
//  **طريقة الإرسال**: يتم إرسال البيانات كجسم (body) للطلب HTTP، وليس كجزء من عنوان URL.
// 2. **عرض البيانات**: البيانات المُرسلة غير مرئية في شريط العنوان، مما يجعلها أكثر أمانًا لإرسال معلومات حساسة.
// 3. **الطول المحدد**: لا توجد قيود تقريبًا على كمية البيانات المُرسلة، مما يجعل POST مناسبًا لإرسال كميات كبيرة من البيانات.
// 4. **التخزين المؤقت**: طلبات POST لا تُخزن في ذاكرة التخزين المؤقت للمتصفح ولا تُضاف إلى سجل المتصفح.
// 5. **استخدامات شائعة**: تُستخدم عادةً لإرسال البيانات إلى الخادم لإنشاء أو تحديث مورد، مثل إرسال نموذج تسجيل أو تحميل ملف.

if (isset($_POST['name'])) {
    echo 'Hello, ' . htmlspecialchars($_POST['name']) . '!';
}
// بشكل عام، تستخدم GET لعمليات الجلب (الاستعلامات والبحث) حيث لا تُعدل البيانات على الخادم، بينما تُستخدم POST لعمليات الإرسال (التسجيل والتحديث) حيث قد تُعدل البيانات على الخادم.

?>